  <section class="faq-container">
    <details class="faq1">
      <summary class="question1">Ik ben iets kwijt geraakt tijdens het evenement. Wat moet ik doen?</summary>
      <p class="answered1">Stuur ons de volgende werkdag een e-mail naar info@ea-events.com met een duidelijke
        omschrijving van de dingen die je bent kwijtgeraakt en je contactgegevens. U kunt ook rechtstreeks contact
        opnemen met de locatie, aangezien zij in het bezit zijn van de verloren en gevonden voorwerpen.</p>
    </details>
    <details class="faq2">
      <summary class="question1">Is er een kledingvoorschift?</summary>
      <p class="answered2">We hebben geen specifieke dresscode voor onze evenementen, maar we raden je wel aan om je
        netjes te kleden!</p>
    </details>
    <details class="faq3">
      <summary class="question1">Kan ik mijn ticket terug betalen als ik om welke reden dan ook niet naat het evenement
        kan komen?</summary>
      <p class="answered2">Vitality Events werkt met een niet-restitutiebeleid en daarom is het niet mogelijk om het
        ticket te restitueren.</p>
    </details>
    <details class="faq4">
      <summary class="question1">Een fotograaf heeft een foto van mij gemaakt, maar ik kan hem niet vinden iop de
        website of Facebook-pagina</summary>
      <p class="answered2">Onze fotografen maken honderden foto's waarvan slechts een kleine selectie online wordt
        gedeeld. Het is mogelijk dat uw foto niet is gesneden.</p>
    </details>
    <details class="faq5">
      <summary class="question1">Moet ik de tickets afdrukken om deel te nemen aan het evenement?</summary>
      <p class="answered2">Nee, het is niet verplicht om de tickets af te drukken om aan het evenement deel te nemen. U
        moet er echter voor zorgen dat de ticketcode duidelijk leesbaar is op uw telefoon of op gedrukt papier.</p>
    </details>
    <details class="faq6">
      <summary class="question1">Waar kan ik fotografie en persaccreditatie aanvragen? </summary>
      <p class="answered2">Stuur ons dan een e-mail naar info@vitality-events.com. Vergeet niet uw portfolio en
        informatie over het mediaplatform op te nemen. We accepteren alleen grotere mediaplatforms en ervaren fotografen
      </p>
    </details>
    <details class="faq7">
      <summary class="question1">Ik heb een kaartje gekocht, maar ik heb nog geen e-mail met het kaartje ontvangen. Wat
        moet ik doen?</summary>
      <p class="answered2">Maak je geen zorgen. Sommige e-mails van de ticketaanbieders kunnen in uw spam-inbox
        terechtkomen. Als je je spam-inbox al hebt gecontroleerd en nog steeds geen e-mail hebt ontvangen, neem dan
        contact op met de relevante ticketaanbieder waar je je ticket hebt gekocht. Vermeld uw boekingsreferentienummer
        en volledige naam wanneer u contact opneemt met de ticketaanbieder.</p>
    </details>
